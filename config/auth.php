            'expire' => 60, // Tempo de expiração do token (60 minutos)
            'throttle' => 60, // Tempo de espera para gerar novos tokens (60 segundos)
        ],
    ],

    /*
    |--------------------------------------------------------------------------
    | Password Confirmation Timeout
    |--------------------------------------------------------------------------
